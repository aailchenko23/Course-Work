
public class Passenger {
    String name;
    String nationality;
    // Конструктор класу
    Passenger(String name, String nationality) {
        this.name = name;
        this.nationality = nationality;
    }

    public String getName() { // Метод для отримання імені пасажира
        return name;
    }

    public String getNationality() { // Метод для отримання національності пасажира
        return nationality;
    }
 }






